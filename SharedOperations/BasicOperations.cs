using System;

namespace SharedOperations
{
    public static class BasicOperations
    {
        public static int Add(int i, int j) => i + j;
        public static int Sub(int i, int j) => i - j;
        public static int Mult(int i, int j) => i * j;
        public static int Div(int i, int j) => i / j;
    }
}
